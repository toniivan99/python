class Car:
    def __init__(self, brand, model, consumption, licensePlate, pricePerHour, pricePerDay, pricePerWeek, isRented, rentedBy,
                 rentedFrom, rentedTo):
        self.brand = brand
        self.model = model
        self.consumption = consumption
        self.licensePlate = licensePlate
        self.pricePerHour = pricePerHour
        self.pricePerDay = pricePerDay
        self.pricePerWeek = pricePerWeek
        self.isRented = isRented
        self.rentedBy = rentedBy
        self.rentedFrom = rentedFrom
        self.rentedTo = rentedTo
